package Matematika;

public class Operasi  {
    int tambah (int b01, int b02){
        return  b01 + b02;
    }
    int bagi (int b01, int b02){
        return b01/b02;
    }
    int kali(int b01, int b02){
        return b01*b02;
    }
    int kurang(int b01, int b02){
        return  b01-b02;
    }



}
